            'nombre' => $nombre,
            'apellidos' => $apellido1 . " " .  $apellido2,
            'email' => strtr(strtolower($nombre), $unwanted_array) . "." . strtr(strtolower($apellido1), $unwanted_array) . "@igobide.com",
